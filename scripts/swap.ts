const TokenFinalAddress = "0xd00ae08403b9bbb9124bb305c09058e32c39a48c";

//Iniciar las instancias de los contratos

async function Swap(wallet) {
  try {
    // Obtener el balance del token inicial
    console.log("Obteniendo balance del token inicial");
    const TokenAContract = new ethers.Contract(
      TokenInitialAddress,
      TokenInitialABI,
      wallet
    );
    const balanceWallet = await TokenAContract.balanceOf(wallet.address);
    console.log("Balance Wallet: ", balanceWallet.toString());

    // Iniciar la instancia del contrato JoeRouter con la wallet actual
    const JoeRouterContract = new ethers.Contract(
      JoeRouterAddress,
      JoeRouterABI,
      wallet
    );

    // Aprobaciones
    console.log("Iniciando aprobaciones");
    await (
      await TokenAContract.approve(JoeRouterAddress, balanceWallet)
    ).wait();
    console.log("Aprobaciones exitosas");

    // Fecha límite para la transacción
    const deadline = Math.floor(Date.now() / 1000) + 60 * 20; // 20 minutos desde ahora

    // Obtener la cantidad mínima de salida para el swap
    // const amountsOut = await JoeRouterContract.getAmountsOut(balanceWallet, [
    //   TokenInitialAddress,
    //   TokenFinalAddress,
    // ]);
    // const amountOutMin = amountsOut[1]; // Puedes ajustar esto para manejar la tolerancia al deslizamiento (slippage)

    // Realizar el swap
    console.log("Iniciando transacción de swap");

    const swap = ethers.parseUnits("1000",18);
    const tx = await JoeRouterContract.swapExactTokensForTokens(
      swap,
      0, // Puedes poner 0 si estás dispuesto a aceptar cualquier cantidad de tokens de salida
      [TokenInitialAddress, TokenFinalAddress],
      wallet.address,
      deadline
    );

    // Confirmación de transacción
    const receipt = await tx.wait();
    console.log(
      `Swap realizado con éxito. Hash de la transacción: ${receipt.transactionHash}`
    );
  } catch (error) {
    console.error("Error al realizar el swap:", error);
  }
}

async function delay(ms) {
  return new Promise(resolve => setTimeout(resolve, ms));
}

async function processWallets(wallets) {
  for (const walletData of wallets) {
    await Swap(walletData.wallet);
    await delay(5000); // Espera de 5 segundos entre cada wallet
  }
}

processWallets(wallets);
